#ifndef VECTORS_H_
#define VECTORS_H_
/**
 * vectors.h
 * =========
 * Defines vectors for 2D and 3D data types.
 *
 * By JacquelineW.
 **/

typedef struct Float2D {
	float x;
	float y;
} float2d_t;

typedef struct Float3D {
	float x;
	float y;
	float z;
} float3d_t;

typedef struct UInt2D {
	unsigned int x;
	unsigned int y;
} uint2d_t;

typedef struct UInt3D {
	unsigned int x;
	unsigned int y;
	unsigned int z;
} uint3d_t;

#endif //VECTORS_H_
